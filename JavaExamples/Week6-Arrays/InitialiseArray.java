/**
 * This class shows several different ways to initialise an
 * array in Java.
 */
public class InitialiseArray {
	public static void main(String[] args) {
		// All examples give a storage space of 5 and use the primitive type int.
		int[] example1 = new int[5];
		int example2[] = new int[5];
		
		// This actually initialises the values in the array.
		int[] example3 = {1, 2, 3, 4, 5};
		int example4[] = {1, 2, 3, 4, 5};
	}
}
